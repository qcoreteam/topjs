let ltrimRegex =  /^[\x09\x0a\x0b\x0c\x0d\x20\xa0\u1680\u180e\u2000\u2001\u2002\u2003\u2004\u2005\u2006\u2007\u2008\u2009\u200a\u2028\u2029\u202f\u205f\u3000]+|[\x09\x0a\x0b\x0c\x0d\x20\xa0\u1680\u180e\u2000\u2001\u2002\u2003\u2004\u2005\u2006\u2007\u2008\u2009\u200a\u2028\u2029\u202f\u205f\u3000]+/g;
let rtrimRegex =  /[\x09\x0a\x0b\x0c\x0d\x20\xa0\u1680\u180e\u2000\u2001\u2002\u2003\u2004\u2005\u2006\u2007\u2008\u2009\u200a\u2028\u2029\u202f\u205f\u3000]+|[\x09\x0a\x0b\x0c\x0d\x20\xa0\u1680\u180e\u2000\u2001\u2002\u2003\u2004\u2005\u2006\u2007\u2008\u2009\u200a\u2028\u2029\u202f\u205f\u3000]+$/g;

export function is_object(target)
{
   return typeof target == "object";
}

export function trim(str)
{
   if(str){
      str = str.replace(trimRegex, "");
   }
   return str || "";
}

export function ltrim(str)
{
   if(str){
      str = str.replace(ltrimRegex, "");
   }
   return str || "";
}

export function rtrim(str)
{
   if(str){
      str = str.replace(rtrimRegex, "");
   }
   return str || "";
}

export function in_array(item, arr)
{
   return arrayPrototype.indexOf.call(arr, item) !== -1;
}